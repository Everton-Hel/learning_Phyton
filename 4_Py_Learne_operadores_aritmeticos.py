# Precedencia de operadores aritméticos
  # 1. (n + n)
  # 2. **
  # 3. * / // %
  # 4. + -
conta_1 = (1 + int(0.5 + 0.5)) ** (5 + 5)
print(conta_1)

# adicao
adicao = 10 + 10
print('Adição', adicao)

# subtracao
subtracao = 10 - 5
print('Subtração', subtracao)

# multiplicacao
multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

# divisao
divisao = 10 / 3  # float
print('Divisão', divisao)

#divisao_inteira
divisao_inteira = 10 // 3
print('Divisão inteira', divisao_inteira)

# exponenciacao
exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

# modulo
modulo = 55 % 2  # resto da divisão
print('Módulo', modulo)

print(10 % 8 == 0)
print(16 % 8 == 0)
print(10 % 2 == 0)
print(15 % 2 == 0)
print(16 % 2 == 0)

